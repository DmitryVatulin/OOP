#include <iostream>
#include <cmath>
using namespace std;


class Shape{
protected:
	int a1, a2, a3, a4;
public:
	Shape() {
		cout << "Enter the length of the sides: " << endl;
		cin >> a1;
		cin >> a2;
		cin >> a3;
		cin >> a4;
	} 
	virtual void square() = 0;


};
class Romb:public Shape {
public:
	double sinus;
	Romb() :Shape() {
		cout << "Enter a smaller angle: " << endl;
		cin >> sinus;
	}
	void square() {
		cout <<  a1*a1*sin(sinus)<<endl;;
	}
};

class Rectangle :public Shape {
public:
	Rectangle() :Shape() {

	}
	void square() {
		cout<< a1*a3<<endl;
	}
};
class Figure {
public:

	void square(Shape *shape) {
		cout << "Area is equal to: ";
		shape->square();
	}

};

